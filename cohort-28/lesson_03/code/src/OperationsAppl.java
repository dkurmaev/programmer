
public class OperationsAppl {
	public static void main(String[] args) {
		int x = 20;
		int y = 10;
		int z = x + y;
		System.out.println(z);
		z = y - x;
		System.out.println(z);
		z = y * x;
		System.out.println(z);
		z = y / x;
		System.out.println(z);
		double a = y / (x * 1.);
		System.out.println(a);
		a = (y + x) / 10;
		System.out.println(a);
		z = 18 % 7;
		System.out.println(z);
		z = 18 / 7;
		System.out.println(z);
		z = 17 % 5;
		System.out.println(z);
		z = 2 % 5;
		System.out.println(z);
	}
}
