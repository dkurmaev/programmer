
public class CompareAppl {
	public static void main(String[] args) {
	     int x = 10;
	     x = x + 5;
	     System.out.println(x);
	     boolean check = x > 10;
	     System.out.println(check);
	     check = x == 10;
	     System.out.println(check);
	     check = x > 15;
	     System.out.println(check);
	     check = x >= 15;
	     System.out.println(check);
	     check = x != 15;
	     System.out.println(check);
	     check = x <= 28;
	     System.out.println(check);
	     x = -x;
	     System.out.println(x);
	     x = -x * 2;
	     System.out.println(x);
	   }
}
