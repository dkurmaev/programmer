
public class VariableAppl {

	public static void main(String[] args) {
	     int a;
	     a = 42;
	     System.out.println(a);
	     a = 10;
	     System.out.println(a);
	     double pi = 3.1415926;
	     System.out.println(pi);
	     double gr = 6.67E43; // 6,67 x 10 in power 43
	     System.out.println(gr);
	   }

}
